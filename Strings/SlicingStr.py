#Slice from the start
#Example:- Get the charactes from the start to position 5(not included):
b = "Hello World"
print(b[:5])

#Slice from the End
#Get the characters from position 2, and all the way to the end:
c = "Hello World"
print(c[2:])

#Negative indexing
x = "Hello World"
print(x[-5:-2])