#Upper Case
#upper()
a = "Hello World"
print(a.upper())

#Lower Case
#lower()
b = "Hello World"
print(b.lower())

#Remove Whitespace
#strip()
c = "Hello World"
print(c.strip())

#Replace String
#replace()
x = "Sohit"
print(x.replace("S","M"))

#Split String
#split()
y = "Hello World"
print(y.split())

#Format Strings
#Example:- Create an f-string
age = 20
txt = f"My name is Sohit, I am {age}"
print(txt)

